#!/usr/bin/env python3
"""Extract conversation summary from a Codex rollout JSONL file.

Usage:
    extract-context.py <rollout.jsonl> [--max-turns N] [--max-chars N]

Outputs a Markdown summary suitable for injection into AGENTS.md so a
new provider session can pick up where the previous one left off.
"""

import argparse
import json
import sys
from pathlib import Path


def extract_turns(path: str, max_turns: int = 20) -> list[dict]:
    turns = []
    with open(path) as f:
        for line in f:
            try:
                ev = json.loads(line)
            except Exception:
                continue
            if ev.get("type") != "response_item":
                continue
            p = ev.get("payload") or {}
            if p.get("type") != "message":
                continue
            role = p.get("role", "")
            if role not in ("user", "assistant"):
                continue
            content_parts = p.get("content") or []
            texts = []
            for part in content_parts:
                if isinstance(part, dict):
                    t = part.get("text", "")
                    if t and not t.startswith("<environment_context>"):
                        texts.append(t)
                elif isinstance(part, str):
                    texts.append(part)
            text = "\n".join(texts).strip()
            if text:
                turns.append({"role": role, "text": text})
    return turns[-max_turns:]


def format_markdown(turns: list[dict], max_chars: int, provider: str) -> str:
    lines = [
        "<!-- cxpod:context-bridge:start -->",
        "## Previous Session Context (auto-generated by cxpod)",
        "",
        f"The user was previously working with **{provider}**. "
        "Below is a summary of the recent conversation. "
        "Use this context to maintain continuity.",
        "",
    ]
    total = 0
    for turn in turns:
        prefix = "**User:**" if turn["role"] == "user" else "**Assistant:**"
        text = turn["text"]
        if total + len(text) > max_chars:
            remaining = max_chars - total
            if remaining > 50:
                text = text[:remaining] + "..."
            else:
                break
        lines.append(f"{prefix} {text}")
        lines.append("")
        total += len(text)
    lines.append("<!-- cxpod:context-bridge:end -->")
    return "\n".join(lines)


def inject_into_agents_md(project_dir: str, block: str) -> None:
    """Write or replace the cxpod context-bridge block in AGENTS.md."""
    agents_path = Path(project_dir) / "AGENTS.md"
    start_marker = "<!-- cxpod:context-bridge:start -->"
    end_marker = "<!-- cxpod:context-bridge:end -->"

    if agents_path.exists():
        content = agents_path.read_text()
        si = content.find(start_marker)
        ei = content.find(end_marker)
        if si != -1 and ei != -1:
            content = content[:si] + block + content[ei + len(end_marker) :]
        else:
            content = content.rstrip() + "\n\n" + block + "\n"
    else:
        content = block + "\n"

    agents_path.write_text(content)


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("rollout", help="Path to rollout JSONL file")
    parser.add_argument("--max-turns", type=int, default=20)
    parser.add_argument("--max-chars", type=int, default=4000)
    parser.add_argument("--provider", default="unknown")
    parser.add_argument("--inject", metavar="DIR",
                        help="Inject into AGENTS.md in this directory")
    args = parser.parse_args()

    turns = extract_turns(args.rollout, args.max_turns)
    if not turns:
        sys.exit(0)

    md = format_markdown(turns, args.max_chars, args.provider)

    if args.inject:
        inject_into_agents_md(args.inject, md)
    else:
        print(md)


if __name__ == "__main__":
    main()
